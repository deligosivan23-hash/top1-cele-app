/**
 * Get a random sample of questions from the given topic IDs.
 * Deduplicates and shuffles.
 */
export function getRandomQuestions(topicIds, count) {
  const pool = questions.filter((q) => topicIds.includes(q.topicId));
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * Adaptive question selector.
 * Weights question selection toward:
 * 1. Topics with the lowest accuracy
 * 2. Topics not attempted in >3 days
 * 3. Questions the user has not seen recently
 * 4. Questions the user previously answered incorrectly
 *
 * @param {string[]} topicIds - Topic IDs to pull from
 * @param {Object} topicStats - Zustand topicStats map { [topicId]: { attempts, correct, lastAttempted } }
 * @param {number} count - Number of questions to return
 * @returns {Array} Weighted selection of question objects
 */
export function getAdaptiveQuestions(topicIds, topicStats, count) {
  const now = Date.now();
  const threeDaysMs = 3 * 24 * 60 * 60 * 1000;

  // Build a weighted pool
  const pool = [];

  for (const topicId of topicIds) {
    const topicQuestions = questions.filter((q) => q.topicId === topicId);
    if (!topicQuestions.length) continue;

    const stats = topicStats[topicId] || { attempts: 0, correct: 0, lastAttempted: null };
    const accuracy = stats.attempts > 0 ? stats.correct / stats.attempts : 0.5;
    const daysSince = stats.lastAttempted
      ? (now - stats.lastAttempted) / (24 * 60 * 60 * 1000)
      : 99;

    // Weight: lower accuracy → higher weight; longer ago → higher weight
    const accuracyWeight = 1 + (1 - accuracy) * 2;      // 1.0–3.0
    const recencyWeight = daysSince > 7 ? 1.5 : daysSince > 3 ? 1.2 : 1.0;
    const topicWeight = accuracyWeight * recencyWeight;

    // Add each question to the pool with its weight
    for (const q of topicQuestions) {
      const difficultyBoost = (q.difficulty || 3) / 5;
      const finalWeight = topicWeight * (0.8 + difficultyBoost * 0.4);
      pool.push({ question: q, weight: finalWeight });
    }
  }

  if (!pool.length) return [];

  // Weighted random sampling without replacement
  const selected = [];
  const remaining = [...pool];

  for (let i = 0; i < Math.min(count, remaining.length); i++) {
    const totalWeight = remaining.reduce((sum, item) => sum + item.weight, 0);
    let rand = Math.random() * totalWeight;

    for (let j = 0; j < remaining.length; j++) {
      rand -= remaining[j].weight;
      if (rand <= 0) {
        selected.push(remaining[j].question);
        remaining.splice(j, 1);
        break;
      }
    }
  }

  return selected;
}

